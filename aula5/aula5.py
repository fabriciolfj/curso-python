"""
+, -, *, /, //, **, %, ()
"""
print("Multiplicacao *", 10 * 10)
print("Divisao /", 10 / 10)
print("Adicao +", 10 + 10)
print("Subtracao -", 10 - 10)
print('Multiplicacao/repeticao', 10 * '10')
print(10.5 // 3)  # divisao inteira, arredonda
print(10.5 / 3)  # retorna ponto flutuante
print(2 ** 5)  # 2 elevado a 5
print(10 % 3) #pegando o resto da divisao
print((5 + 2) * 10)
